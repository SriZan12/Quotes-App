package com.srijan.quotes;

public class QuoteList {
    private String text;
    private String author;


    // Getter Methods

    public String getText() {
        return text;
    }

    public String getAuthor() {
        return author;
    }

    // Setter Methods

    public void setText(String text) {
        this.text = text;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
}
